JOIN USRFINDATA.ODS_OFICINA@BSIBPDW0@UFINRIE B ON A.COD_OFICINA=B.COD_OFICINA AND B.FECPRO='30/06/2022' AND B.COD_BANCA IN ('2685','2661')
